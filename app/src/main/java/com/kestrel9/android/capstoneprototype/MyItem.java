package com.kestrel9.android.capstoneprototype;

public class MyItem {
    String id;
    String content;

    public MyItem(String content) {
        this.content = content;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
